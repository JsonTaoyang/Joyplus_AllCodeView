package com.joyplus.model;


public class IpLocationMapping {
	

	private String ip_from;

	private String ip_to;

	private String province;

	private String city;

	private long ip_from_long;

	private long ip_to_long;

	public String getIp_from() {
		return ip_from;
	}

	public void setIp_from(String ip_from) {
		this.ip_from = ip_from;
	}

	public String getIp_to() {
		return ip_to;
	}

	public void setIp_to(String ip_to) {
		this.ip_to = ip_to;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public long getIp_from_long() {
		return ip_from_long;
	}

	public void setIp_from_long(long ip_from_long) {
		this.ip_from_long = ip_from_long;
	}

	public long getIp_to_long() {
		return ip_to_long;
	}

	public void setIp_to_long(long ip_to_long) {
		this.ip_to_long = ip_to_long;
	}
	



}
